#include "fractol.h"

double	carpet(int x, int y)
{
	if (!((x / 1) % 3 == 1 && (y / 1) % 3 == 1) && !((x / 3) % 3 == 1
		&& (y / 3) % 3 == 1) && !((x / 9) % 3 == 1 && (y / 9) % 3 == 1)
		&& !((x / 27) % 3 == 1 && (y / 27) % 3 == 1)
		&& !((x / 81) % 3 == 1 && (y / 81) % 3 == 1)
		&& !((x / 243) % 3 == 1 && (y / 243) % 3 == 1))
		return (1);
	else
		return (0);
}
